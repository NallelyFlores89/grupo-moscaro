          <p id="qaf">Preguntas frecuentes sobre HEMOSURE iFOB</p>
          <p class="pregunta">1.- ¿Cuál es la ventaja del aumento de la especifidad de la prueba del HEMOSURE iFOB?</p>
          <p class="respuesta">La prueba de HEMOSURE iFOB de Hemosure tiene un alto porcentaje de especifidad para la sangre humana
          (>96%). La importancia de una prueba tan especifica es que ayuda a detectar falsos positivos. Si no hay 
          sangre humana presente, la prueba arrojará un resultado negativo, esto es posible debido a la alat especifidad
          hacia la hemoglobina humana</p>
          <p class="pregunta">2.- ¿Cuál es la ventaja del aumento de la especifidad de la prueba del HEMOSURE iFOB?</p>
          <p class="respuesta">La prueba de HEMOSURE iFOB de Hemosure tiene un alto porcentaje de especifidad para la sangre humana
          (>87%). Para que una prueba sea precisa debe de tener una alta sensibilidad para detectar resultados positivos 
          y descartar los falsos negativos. Cuando existe algún pólipo o cáncer y existe sangrado de tubo digestivo por 
          mínimo que sea, la prueba arrojará positivos debido a su alta sensibilidad</p>
          <p class="pregunta">3.- ¿La prueba del HEMOSURE iFOB es más fácil de leer que la prueba de guayaco?</p>
          <p class="respuesta">Sí. Una sola línea debajo de la zona del control C del Casete o Tira indica una prueba negativa.
          Dos líneas en el Casete o Tira (en la zona C y en la zona T), indican una prueba positiva para sangre humana. Aún
          cuando la línea del Test (examen) puede no tener un color tan inteso como la línea debajo de Control, se considera 
          una prueba positiva. Todas las pruebas deben ser leídas dentro de cinco (5) minutos después de haber agregado la 
          muestra del paciente. Un intestino normal tiene una pérdida de hasta 2ml de sangre por día. Si la prueba es leída
          después de cinco minutos, una línea borrosa y oscurecida se puede revelar debajo de la zona Test (examen) en el Casete.
          Esto es considerado una "línea fantasma" y NO debe ser considerada una prueba positiva</p>
          <p class="pregunta">¿Cómo puede la prueba de HEMOSURE iFOB de Hemosure distinguir la sangre de tracto gastrointestinal superior
          (entre la boca y el estómago) del inferior (entre el duodeno y el ano)?</p>
          <p class="respuesta">El anticuerpo usado en la membrana del Casete de prueba de Hemosure es muy especificado hacia el
          grupo hemo contenido en la hemoglobina de los glóbulos rojos cuando están intactos. La sangre después de pasar por el 
          duodeno altera la estructura química de la hemoglobina, rompiendo el anillo pirrólico y liberando al grupo hemo, haciéndolo
          incapaz de reaccionar con el anticuerpo. Esto dará un resultado negativo para la parte superior del tracto gastrointestinal.</p>
